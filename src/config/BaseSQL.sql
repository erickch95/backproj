CREATE TABLE TablaUsuarios(
    id_usuario int NOT NULL AUTO_INCREMENT,
    cedula varchar(16) NOT NULL UNIQUE,
    clave varchar(96) NOT NULL,
    nombre varchar (32) NOT NULL,
    telefono varchar(32) DEFAULT "No agregado." NOT NULL,
    bloqueado BOOLEAN DEFAULT false NOT NULL,
    usuario_creado DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    usuario_modificado DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    id_usuario_modificado_por INT DEFAULT 0 NOT NULL,
    rol_usuario_modificado_por varchar (32) DEFAULT "Usuario Creado por SQL." NOT NULL,
    rol_usuario varchar(16) DEFAULT "Fiscal",
    PRIMARY KEY (id_usuario)
    ); 

ALTER TABLE TablaUsuarios AUTO_INCREMENT=100;

CREATE TABLE TablaConminatorias (
    id_conminatoria INT NOT NULL AUTO_INCREMENT,
    codigo varchar(32) NOT NULL,
    denunciados varchar(255) DEFAULT "No se agregaron denunciados." NOT NULL,
    denunciantes varchar(255) DEFAULT "No se agregaron denunciantes." NOT NULL,
    delitos varchar(128) DEFAULT "No se agrego el delito. "NOT NULL,
    fecha_cd DATE NOT NULL,
    fecha_fiscal DATE NOT NULL,
    fecha_limite DATE NOT NULL,
    realizado BOOLEAN DEFAULT false NOT NULL,
    fecha_realizado DATETIME,
    id_usuario_marcado_por INT DEFAULT 0 NOT NULL,
    rol_usuario_marcado_por varchar(32) DEFAULT "No marcado." NOT NULL,
    oculto BOOLEAN DEFAULT false NOT NULL,
    directorio_pdf varchar(64) DEFAULT "http://192.168.166.66:4000/upload/103Z82GNugYGIN63SCB64Y3F.pdf" NOT NULL,
    conminatoria_creado DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
    conminatoria_modificado DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    id_usuario_conminatoria_modificado_por INT DEFAULT 0 NOT NULL, 
    rol_usuario_conminatoria_modificado_por varchar (32) DEFAULT "Conminatoria creada por SQL." NOT NULL,
    id_usuario_conminatoria INT NOT NULL,
    PRIMARY KEY (id_conminatoria),
    FOREIGN KEY (id_usuario_conminatoria) REFERENCES TablaUsuarios(id_usuario)
); 

